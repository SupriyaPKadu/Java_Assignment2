package Assignment27_09_24;

public class Pattern_3 {

	public static void main(String[] args) {

		for (int i = 1; i <= 5; i++) // row
		{
			for (int j = 1; j <= 5; j++) // col
			{
				System.out.print(i+" ");
			}
			System.out.println();
		}
	}

}
