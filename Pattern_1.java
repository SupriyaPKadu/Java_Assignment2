package Assignment27_09_24;

public class Pattern_1 {

	public static void main(String[] args) {

		for (int i = 0; i < 3; i++) // row
		{
			for (int j = 0; j < 5; j++) // col
			{
				System.out.print(" * ");
			}
			System.out.println();
		}
	}
}
